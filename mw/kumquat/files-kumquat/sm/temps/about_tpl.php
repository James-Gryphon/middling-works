<div class="title_header"><h3>About Synchronous Messages</h3></div><br>
<div class="main_box_content single half_width">
<div class="con_box left">
<p>Synchronous Messages allows you to send information to someone that they will not receive until they have reciprocated. This is useful for gaming applications (like Rock, Paper, Scissors), but other inventive uses have been discovered for it, like preparing secret messages for someone's birthday, that would be revealed all at once to the recipient once they had followed instructions.</p>

<p>Due to the security problems from accepting user input, this is focused on supporting plain-text messages. If you include any formatting, it will be delivered as text, and instead of seeing beautiful bold text, or a Bitcoin mining script, your colleague will only see how you attempted to flout the system.</p>

<p>Batches are customarily removed about a week after they were last updated, but they may be removed more or less frequently.</p>

