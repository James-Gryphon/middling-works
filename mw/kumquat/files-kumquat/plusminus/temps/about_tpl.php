<div class="title_header"><h3>About The Plus-Minus Game</h3></div><br>
<div class="main_box_content single half_width">
<div class="con_box left">
<p>The Plus-Minus Game is an automatic implementation of a classic counting game continually reinvented and played on many online message boards. Two teams are trying to raise or lower the count, and do so by adding or subtracting from the number, until the number is reached which their side is aiming for.</p>
<p>In this case, a player can pick either side he wants, and even switch sides mid-game without restriction. However, as the game keeps score and records each player's impact on the game, a player who wants to maximize their impact should remain loyal to one team during a game.</p>
<p>The target numbers in many forum games are 0 and 100, but in this case, the floor target (the goal the subtractors want to reach) is a randomly chosen number, the ceiling target is the number 100 greater than that, and the starting point is the number exactly between them.
